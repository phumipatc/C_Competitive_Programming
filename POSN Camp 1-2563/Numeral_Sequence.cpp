LL minN(vector<LL > a){
	LL minn = 1e18;
	for(auto x:a)
		minn = min(minn,x);
	return minn;
}
LL maxN(vector<LL > a){
	LL maxx = -1e18;
	for(auto x:a)
		maxx = max(maxx,x);
	return maxx;
}
LL modN(LL a,LL b,LL c = MOD){
	if(b == 0)	return 0;
	if(b == 1)	return a%c;
	LL now = modN(a,b/2);
	if(b&1)	return (((now*now)%c)*(a%c))%c;
	else	return (now*now)%c;
}
LL a[12],last[12],p[12];
void solve(){
	int n;
	cin >> n;
	p[0] = 1;
	for(int i=1;i<=9;i++){
		a[i] = 9*p[i-1]*i+a[i-1];
		last[i] = last[i-1]*10 + 9;
		p[i] = p[i-1]*10;
	}
	int idx = lower_bound(a+1,a+10,n)-a;
	n-=a[idx-1];
	LL now = last[idx-1]+ceil((double )n/idx);
	// printf("%lld %d %d %d ",now,idx,n,p[idx-n]);
	n%=idx;
	if(!n)	n = idx;
	cout << (now/p[idx-n])%10 << '\n';
}
int main(){
	ios_base::sync_with_stdio(0);	cin.tie(0),cout.tie(0);
	int q = 1;
	cin >> q;
	for(int Q=1;Q<=q;Q++){
		// cout << "Case #" << Q << ": ";
		solve();
	}
	return 0;
}
